// Using the global console object to log messages
console.log("Hello, World!");

// Using setTimeout to execute a function after 2 seconds
setTimeout(() => {
  console.log("Delayed message after 2 seconds");
}, 2000);

// Using __dirname to get the current directory name
console.log("__dirname:", __dirname);

// Using __filename to get the current file name
console.log("__filename:", __filename);

// Using process object to access environment variables
console.log("Node.js version:", process.version);

// Using process.argv to access command-line arguments
console.log("Command-line arguments:", process.argv);
